const QUICK_CHIPS = [
  { id: 'authority', label: 'Beh√∂rde' },
  { id: 'boss', label: 'Chef' },
  { id: 'landlord', label: 'Vermieter' },
  { id: 'customer', label: 'Kunde' },
];

